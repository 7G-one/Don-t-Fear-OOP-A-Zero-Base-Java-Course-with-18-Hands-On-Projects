// =============================================
// 第 00 课：Java 零基础入门
// =============================================
// 这节课是专门为完全零基础的同学准备的。
// 我们将从零开始认识 Java，写出第一个程序。
// 学完这节课，你将能够：
//   1. 理解什么是编程
//   2. 写出一个完整的 Java 程序
//   3. 使用变量存储数据
//   4. 进行简单的计算和判断
//
// 生活类比：
//   编程就像写菜谱——你把每一步都写得清清楚楚，
//   计算机就像一个听话的厨师，按步骤一步步执行。
//   只要菜谱写得对，做出来的菜就一定对！

class HelloWorld {
    public static void main(String[] args) {

        // =============================================
        // 第一节：什么是编程？
        // =============================================
        // 编程（Programming）就是用计算机能理解的语言，
        // 告诉计算机一步一步该做什么事情。
        //
        // 想象一下：你有一个非常听话但又很笨的助手，
        // 你说"倒杯水"，它听不懂。
        // 但如果你说：
        //   1. 走到厨房
        //   2. 打开柜子
        //   3. 拿出杯子
        //   4. 打开水龙头
        //   5. 把水接到杯子里
        //   6. 关闭水龙头
        //   7. 把杯子端过来
        // 它就能完成任务了。编程就是这个过程！
        //
        // Java 语言的特点（记住这几个关键词）：
        //   - 跨平台：写一次，到处运行（Windows、Mac、Linux 都能跑）
        //   - 面向对象：用"对象"来组织代码（后面会详细讲）
        //   - 强类型：每个变量必须声明类型（不能随便存）
        //   - 自动垃圾回收：不用手动清理内存（C/C++ 就要自己管）
        //
        // Java 的主要用途：
        //   - 企业级应用（银行系统、电商平台、ERP 等）
        //   - Android 手机应用开发
        //   - Web 后端开发（服务器端程序）
        //   - 大数据处理（Hadoop、Spark 等框架都用 Java）

        System.out.println("==========================================");
        System.out.println("第 00 课：Java 零基础入门");
        System.out.println("==========================================\n");

        // =============================================
        // 第二节：Java 程序的基本结构
        // =============================================
        // 每个 Java 程序都必须有两个东西：
        //
        //   1. 类（Class）—— 程序的"容器"
        //      - 用 class 关键字定义
        //      - 类名必须和文件名一致（HelloWorld.java 里就是 HelloWorld）
        //      - 类就像一个"模板"或"蓝图"
        //      - 所有代码都必须写在类里面
        //
        //   2. main 方法 —— 程序的"入口"
        //      - 固定写法：public static void main(String[] args)
        //      - 程序从这里开始执行
        //      - 没有 main 方法，程序就不知道从哪里开始
        //      - 就像一本书的"第一章第一节"
        //
        // 结构示意：
        //   public class HelloWorld {        <-- 类的开始
        //       public static void main(String[] args) {  <-- main 方法的开始
        //           // 你的代码写在这里
        //       }                           <-- main 方法的结束
        //   }                               <-- 类的结束

        System.out.println("--- 第二节：Java 程序的基本结构 ---");
        System.out.println("每个 Java 程序都需要一个类和一个 main 方法");
        System.out.println("类 = 模板/蓝图，main 方法 = 程序入口\n");

        // =============================================
        // 第三节：第一个程序 - Hello World
        // =============================================
        // Hello World 是全世界程序员学习新语言时写的第一个程序。
        // 这个传统从 1978 年就开始了！
        //
        // System.out.println() 是 Java 中最常用的输出命令：
        //   - System  = 系统（Java 内置的系统类）
        //   - out     = 输出（output 的缩写）
        //   - println = 打印一行（print line 的缩写）
        //   - 整体意思就是：在控制台打印一行文字
        //
        // 重要规则（必须记住）：
        //   1. 要打印的文字必须用英文双引号括起来 "..."
        //   2. 每条语句必须以英文分号 ; 结尾
        //   3. 所有标点符号必须用英文（不能用中文的逗号、括号、分号）

        System.out.println("--- 第三节：第一个程序 - Hello World ---");
        System.out.println("Hello, World!");
        System.out.println("你好，Java！");

        // =============================================
        // 第四节：打印不同类型的内容
        // =============================================
        // System.out.println() 不仅能打印文字，还能打印数字。
        //
        // 规则：
        //   - 文字（字符串）：必须用双引号括起来，如 "你好"
        //   - 数字：直接写，不需要引号，如 42、3.14
        //   - 拼接：用 + 号可以把文字和数字连在一起
        //
        // 注意：+ 在这里不是"加法"，而是"拼接"（把两边连起来）

        System.out.println("\n--- 第四节：打印不同类型的内容 ---");

        // 打印数字（不需要引号）
        System.out.println(42);          // 打印整数
        System.out.println(3.14);        // 打印小数

        // 打印字符串（需要双引号）
        System.out.println("我是字符串");

        // 字符串拼接（用 + 连接）
        System.out.println("我今年 " + 18 + " 岁");
        System.out.println("圆周率约等于 " + 3.14);
        System.out.println("计算结果：" + (10 + 20));  // 括号里的先计算

        // =============================================
        // 第五节：变量 - 存储数据的盒子
        // =============================================
        // 变量（Variable）就像一个盒子，用来存放数据。
        // 每个盒子都有：
        //   - 类型：盒子里放什么种类的东西
        //   - 名字：盒子的标签，方便找到它
        //   - 值：盒子里实际放的东西
        //
        // Java 是强类型语言，声明变量时必须指定类型。
        // 声明变量的语法：类型 变量名 = 值;
        //
        // 常见的数据类型：
        //   - String  ：字符串（文字），用双引号
        //   - int     ：整数（没有小数点），如 18、-5、0
        //   - double  ：小数（有小数点），如 1.75、3.14
        //   - boolean ：布尔值（只有 true 或 false）

        System.out.println("\n--- 第五节：变量 - 存储数据的盒子 ---");

        // 声明并赋值各种类型的变量
        String name = "小明";        // 字符串类型，存文字
        int age = 18;               // 整数类型，存整数
        double height = 1.75;       // 小数类型，存小数
        boolean isStudent = true;   // 布尔类型，存真/假

        // 打印变量的值（不需要引号，直接写变量名）
        System.out.println("姓名：" + name);
        System.out.println("年龄：" + age);
        System.out.println("身高：" + height + " 米");
        System.out.println("是否学生：" + isStudent);

        // 变量的值可以修改（重新赋值）
        age = 19;  // 把 age 的值改成 19
        System.out.println("修改后的年龄：" + age);

        // =============================================
        // 第六节：简单计算
        // =============================================
        // Java 可以当计算器使用！
        // 常用的运算符：
        //   +   加法
        //   -   减法
        //   *   乘法
        //   /   除法（注意：整数除整数，结果还是整数！）
        //   %   取余数（求模运算，如 10 % 3 = 1）
        //
        // 重要注意：
        //   - int / int = int（整数除法，小数部分被丢弃）
        //     例如：7 / 2 = 3（不是 3.5！）
        //   - double / double = double（小数除法，保留小数）
        //     例如：7.0 / 2.0 = 3.5

        System.out.println("\n--- 第六节：简单计算 ---");

        // 直接计算并打印
        System.out.println("加法：" + (10 + 3));      // 13
        System.out.println("减法：" + (10 - 3));      // 7
        System.out.println("乘法：" + (10 * 3));      // 30

        // 整数除法（结果是整数，小数部分丢失）
        System.out.println("整数除法：" + (7 / 2));   // 3，不是 3.5！

        // 小数除法（结果保留小数）
        System.out.println("小数除法：" + (7.0 / 2.0)); // 3.5

        // 取余数（10 除以 3，商 3 余 1）
        System.out.println("取余数：" + (10 % 3));    // 1

        // 用变量进行计算
        int a = 100;
        int b = 30;
        System.out.println(a + " + " + b + " = " + (a + b));
        System.out.println(a + " - " + b + " = " + (a - b));
        System.out.println(a + " * " + b + " = " + (a * b));
        System.out.println(a + " / " + b + " = " + (a / b));
        System.out.println(a + " % " + b + " = " + (a % b));

        // =============================================
        // 第七节：条件判断
        // =============================================
        // 条件判断让程序能够根据不同情况做出不同反应。
        // 就像日常生活中的"如果...就...否则就..."：
        //   如果下雨，就带伞；否则就不带。
        //
        // Java 中的条件判断语法：
        //   if (条件) {
        //       条件为真时执行的代码
        //   }
        //
        //   if (条件) {
        //       条件为真时执行
        //   } else {
        //       条件为假时执行
        //   }
        //
        //   if (条件1) {
        //       条件1为真时执行
        //   } else if (条件2) {
        //       条件2为真时执行
        //   } else {
        //       以上都不满足时执行
        //   }
        //
        // 常见的比较运算符：
        //   ==  等于    !=  不等于
        //   >   大于    <   小于
        //   >=  大于等于  <=  小于等于

        System.out.println("\n--- 第七节：条件判断 ---");

        // 示例1：判断是否成年
        int checkAge = 18;
        if (checkAge >= 18) {
            System.out.println("年龄 " + checkAge + "：已成年");
        } else {
            System.out.println("年龄 " + checkAge + "：未成年");
        }

        // 示例2：根据分数判断等级
        int score = 85;
        if (score >= 90) {
            System.out.println("分数 " + score + "：优秀");
        } else if (score >= 80) {
            System.out.println("分数 " + score + "：良好");
        } else if (score >= 60) {
            System.out.println("分数 " + score + "：及格");
        } else {
            System.out.println("分数 " + score + "：不及格");
        }

        // =============================================
        // 第九节：注释
        // =============================================
        // 注释是写给程序员看的说明文字，
        // 计算机在运行时会完全忽略注释。
        // 注释的目的是让代码更容易理解。
        //
        // 两种注释方式：
        //
        // 1. 单行注释：用 // 开头
        //    // 这是一行注释
        //    只影响 // 后面的内容，到行尾结束
        //
        // 2. 多行注释：用 /* 开头，用 */ 结尾
        //    /* 这是
        //       多行注释 */
        //    可以跨越多行
        //
        // 好习惯：给重要的代码写注释，解释"为什么"这样做

        System.out.println("\n--- 第九节：注释 ---");
        // 这是单行注释，计算机不会执行这行
        System.out.println("注释是给程序员看的，计算机忽略注释");
        /* 这是多行注释
           可以写很多行
           计算机同样会忽略 */

        // =============================================
        // 练习参考答案
        // =============================================

        System.out.println("\n==========================================");
        System.out.println("练习参考答案");
        System.out.println("==========================================\n");

        // 练习1：打印个人信息
        System.out.println("--- 练习1 参考 ---");
        System.out.println("我叫小明");
        System.out.println("我今年 " + 18 + " 岁");
        System.out.println("我喜欢编程");

        // 练习2：变量练习
        System.out.println("\n--- 练习2 参考 ---");
        String myName = "小明";
        int myAge = 18;
        String myHobby = "编程";
        System.out.println("我叫" + myName + "，今年" + myAge + "岁，喜欢" + myHobby);

        // =============================================
        // 课程总结
        // =============================================
        /*
         * 核心收获：
         * - Java 程序的基本结构：类 + main 方法
         * - System.out.println() 是输出命令
         * - 变量是存储数据的盒子，Java 是强类型语言
         *
         * 常见陷阱：
         * - 忘记写分号 ;
         * - 中英文标点混用（必须用英文标点）
         * - 字符串和数字拼接时忘记加括号
         *
         * 下节课预告：
         * - 深入学习变量声明、数据类型详解和类型转换
         */

        // =============================================
        // 恭喜完成
        // =============================================
        System.out.println("\n恭喜你完成了第 00 课：Java 零基础入门！");
        System.out.println("下节课我们将学习变量与数据类型。");
    }
}
